package bit.scallionchickenrice.raph.entity;

public class MenuManagement {
	private String DishName;
	
	private String DishClass;
	
	private String DishResource;
	
	private String DishPrice;

	public String getDishName() {
		return DishName;
	}

	public void setDishName(String dishName) {
		DishName = dishName;
	}

	public String getDishClass() {
		return DishClass;
	}

	public void setDishClass(String dishClass) {
		DishClass = dishClass;
	}

	public String getDishResource() {
		return DishResource;
	}

	public void setDishResource(String dishResource) {
		DishResource = dishResource;
	}

	public String getDishPrice() {
		return DishPrice;
	}

	public void setDishPrice(String dishPrice) {
		DishPrice = dishPrice;
	}
}
